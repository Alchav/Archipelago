next_id = 0x7E0000

item_name_to_id: dict[str, int] = {}
location_name_to_id: dict[str, int] = {}

def register(names, isItem):
    global next_id
    for name in names:
        if isItem:
            item_name_to_id[name] = next_id
        else:
            location_name_to_id[name] = next_id
        next_id += 1

